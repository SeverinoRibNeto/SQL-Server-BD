--TABELA MÉDIA
INSERT INTO TblMedia
SELECT pa.matricula_aluno,p.codigo_disciplina,AVG(p.nota) as media
FROM 
TblProvaAluno pa
INNER JOIN
TblProva p
ON pa.id_prova=p.id
INNER JOIN
TblAluno a
ON pa.matricula_aluno=a.matricula
GROUP BY pa.matricula_aluno, p.codigo_disciplina
ORDER BY pa.matricula_aluno
